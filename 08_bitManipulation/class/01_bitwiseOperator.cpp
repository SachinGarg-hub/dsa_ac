#include <iostream>
using namespace std;
int main()
{
    // bitwise operator
    // and
    cout << (3 & 5) << endl;
    // or
    cout << (3 | 5) << endl;
    // xor
    cout << (3 ^ 5) << endl;
    // not
    cout << (~6) << endl;
    // left shift
    cout << (7 << 2) << endl;
    // right shift
    cout << (7 >> 2) << endl;
}